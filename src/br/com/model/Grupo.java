package br.com.model;

import java.util.logging.Logger;

/**
 *
 * @author Diego Rangel  
 */
public class Grupo {
    public int id;
    public String nome;

    public Grupo(int id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
}
