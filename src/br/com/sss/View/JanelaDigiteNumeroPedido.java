    private void campoValorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoValorKeyPressed
        if(evt.getKeyCode() == evt.VK_ENTER){
            botaoOK.requestFocus();
            //preencheu = true;
            //this.dispose();

        }else{
            if(evt.getKeyCode() == evt.VK_ESCAPE){
                botaoOK.requestFocus();
                preencheu = false;
                this.dispose();
            }
        }
    }//GEN-LAST:event_campoValorKeyPressed

    private void botaoOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoOKActionPerformed
        if (campoValor.getText().equals("")){
            botaoOK.requestFocus();
            preencheu = false;
            this.dispose();
        }else{
            botaoOK.requestFocus();
            preencheu = true;
            this.dispose();
        }
    }//GEN-LAST:event_botaoOKActionPerformed

    private void botaoOKKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_botaoOKKeyPressed
        if(evt.getKeyCode() == evt.VK_ENTER){
            if (campoValor.getText().equals("")){
                botaoOK.requestFocus();
                preencheu = false;
                this.dispose();
            }else{
                botaoOK.requestFocus();
                preencheu = true;
                this.dispose();
            }
        }
    }//GEN-LAST:event_botaoOKKeyPressed

